#sum_of_two_num
def add(num_one,num_two):
    sum_of_two_number = num_one + num_two
    print(sum_of_two_number)
num_one = -2
num_two = 3
add(num_one,num_two)

#substract_of_two_num
def subs(num_one,num_two):
    substract_of_two_number = num_one - num_two
    print(substract_of_two_number)
subs(num_one,num_two)

#multiplication_of_two_num
def multi(num_one,num_two):
    multiplication_of_two_number = num_one * num_two
    print(multiplication_of_two_number)
multi(num_one,num_two)

#division_of_a_num_by_a_num
def div(num_one,num_two):
    division_of_two_number = num_one / num_two
    print(division_of_two_number)
div(num_one,num_two)

